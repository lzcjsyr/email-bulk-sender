# ============================================================
# 使用说明
# ============================================================
# 1. 配置发件人姓名
#    - 将 SENDER_NAME 改为您的真实姓名（建议使用英文或拼音）
#
# 2. 变量说明（可在邮件标题和正文中使用）：
#    - {var1}: 收件人姓名（从Excel的"var1"列读取）
#    - {var2}: 自定义字段2（从Excel的"var2"列读取）
#    - {var3}: 自定义字段3（从Excel的"var3"列读取）
#    - {sender_name}: 发件人姓名（即上面的 SENDER_NAME）
#
# 3. 编辑邮件内容
#    - 修改 EMAIL_SUBJECT 为您的邮件标题
#    - 修改 EMAIL_BODY 为您的邮件正文
#
# 4. Excel文件格式要求
#    列顺序：收件邮箱 | var1 | var2 | var3 | 附件名称1 | 附件名称2 | 发送情况
#
#    示例数据：
#    收件邮箱              | var1    | var2      | var3    | 附件名称1 | 附件名称2 | 发送情况
#    user@example.com      | 张三    | 合作伙伴  |         | doc.pdf   |           | 0
#    another@example.com   | 李四    | 客户      | VIP     |           |           | 0
#
#    说明：
#    - var1 建议填写收件人姓名，用于邮件中的称呼
#    - var2、var3 可根据需要填写其他信息，不使用可留空
#    - 附件名称填写 attachments 文件夹中的文件名
#    - 发送情况：0 = 未发送，1 = 已发送（发送成功后自动更新）
#
# 5. 保存此文件后，运行 send_bulk_emails.py 开始发送邮件
